    {
        $paginator = User::paginate(10);
        $users = $paginator->getCollection();

        $data = Fractal::create()
                ->collection($users, new UserTransformer())
                ->paginateWith(new IlluminatePaginatorAdapter($paginator))
                ->toArray();

        return response()->json($data);
